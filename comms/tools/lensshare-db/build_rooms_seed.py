#!/usr/bin/env python3
"""
build_rooms_seed.py

Scan a list of pages (local files or URLs) for links like:
  <a href="room.php?room=happy-hour">Happy Hour</a>

…and generate a PHP $roomsSeed array with unique slugs.

Requirements:
    pip install requests beautifulsoup4
"""

import os
from pathlib import Path
from urllib.parse import urlparse, parse_qs
import requests
from bs4 import BeautifulSoup

# TODO: Edit this list with your actual pages:
